CREATE TABLE `D_IMAGE` (
  `UPDT` char(19) NOT NULL DEFAULT '1970/01/01 00:00:00',
  `ATTR` int(11) NOT NULL DEFAULT 0,
  `ODT` char(19) NOT NULL DEFAULT '1970/01/01 00:00:00',
  `CDT` char(19) NOT NULL DEFAULT '9999/12/31 23:59:59',
  `IID` int(10) unsigned NOT NULL,
  `SECID` int(10) unsigned NOT NULL DEFAULT 0,
  `FILE` longblob NOT NULL,
  `EXT` varchar(10) NOT NULL DEFAULT '',
  `ALT` varchar(100) NOT NULL DEFAULT '',
  `HEIGTH` int(11) unsigned NOT NULL DEFAULT 1,
  `WIDTH` int(11) unsigned NOT NULL DEFAULT 1,
  `SIZE` int(11) unsigned NOT NULL DEFAULT 1,
  PRIMARY KEY (`IID`),
  KEY `IDX_1` (`SECID`,`IID`)
) ENGINE=MyISAM DEFAULT CHARSET=utf8
